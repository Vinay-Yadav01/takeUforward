class Solution {
  public:
    string armstrongNumber(int n){
        int temp = n;
        int arm=0;
        while(temp){
            arm+=pow((temp%10),3);
            temp/=10;
        }
        if(arm==n) return "Yes";
        else return "No";
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        Solution ob;
        cout << ob.armstrongNumber(n) << endl;
    }
    return 0;
}

